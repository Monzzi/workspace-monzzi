import React from 'react';
import './App.css';const PostLayout = ({ children }) => {
  const [progress, setProgress] = React.useState(0);

  React.useEffect(() => {
    const scrollListener = () => {
      const newProgress = (window.scrollY / document.body.scrollHeight) * 100;
      setProgress(newProgress);
    };

    window.addEventListener("scroll", scrollListener, false);
    return () => window.removeEventListener("scroll", scrollListener, false);
  }, []);

  return (
    <div>
      <h2 className="progress">Progress: {progress}%</h2>
      {children}
    </div>
  );
};

